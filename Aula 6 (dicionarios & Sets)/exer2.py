# Você recebeu um dicionário que mapeia países e suas respectivas populações: populacoes 
# = {"Brasil": 215_000_000, "China": 1_400_000_000, "EUA": 333_000_000, "Índia": 1_220_000_000} 
# Seu objetivo é descobrir qual país possui a maior população usando um loop for. Passo a passo sugerido:
#  Crie duas variáveis de controle: Uma para guardar o nome do país com maior população (ex: pais_maior_pop), iniciando com uma string vazia. 
# Outra para guardar o valor da maior população encontrada até agora (ex: maior_pop), iniciando com 0. 
# Percorra o dicionário com um for, acessando o país e sua população. 
# A cada iteração, compare a população atual com o valor de maior_pop: 
# Se a população atual for maior, atualize as duas variáveis (maior_pop e pais_maior_pop). 
# Ao final do laço, exiba na tela o resultado no formato: O país com a maior população é: X com Y habitantes.

maior_população_encontrada = 0
pais_maior_população = ""

paises = {"Brasil": 215000000, "China": 1400000000, "EUA": 333000000, "Índia": 1220000000}

for pais, populacao in paises.items():#quando for usar .items você precisa criar duas variaves uma para keys e uma para values 
    if populacao > maior_população_encontrada: 
        maior_população_encontrada = populacao
        pais_maior_população = pais
print(f"O país com a maior população é: {pais_maior_população} com {maior_população_encontrada} habitantes.")

